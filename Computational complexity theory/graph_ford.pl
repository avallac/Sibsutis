#!/usr/bin/perl

use strict;
use Data::Dumper;

my ($i,$j,$x,$y,$k,@D,$mod);
sub min ($$) { $_[$_[0] > $_[1]] }

my $N = 7;
#         1    2   3     4    5    6    7
my $m = [[   0,  20,1000,1000,1000,   1,   1],
         [  20,   0,   6,1000,1000,1000,   4],
         [1000,   6,   0,   3,1000,1000,   9],
         [1000,1000,   3,   0,  17,1000,  16],
         [1000,1000,1000,  17,   0,  20,  25],
         [   1,1000,1000,1000,  20,   0,  36],
         [   1,   4,   9,  16,  25,  36,   0]];

$mod = 1;
for ($i = 1; $i < $N; $i++) {
    $D[$i] = 1000;
}
$D[0] = 0;
while($mod) {
    $mod = 0;
    for ($i = 0; $i < $N; $i++) {
        for ($j = 0; $j < $N; $j++) {
            if ($D[$i] > $D[$j] + $m->[$i]->[$j]) {
                $D[$i] = $D[$j] + $m->[$i]->[$j];
                $mod = 1;
            }
        }
    }
}
for ($i = 0; $i < $N; $i++) {
    print $D[$i]." ";
}
print "\n";